function [A,B,j,i,movement] = jump(A,B,j,i,movement)
if (movement(i-1,j)==8 && A(B(j,1)-1,B(j,2))<1 && A(B(j,1)-1,B(j,2))>0 && A(B(j,1),B(j,2)+1)==-1 && A(B(j,1),B(j,2)-1)==-1 && A(B(j,1)-1,B(j,2)-1)<1 && A(B(j,1)-1,B(j,2)-1)>0 && A(B(j,1)-1,B(j,2)+1)<1 && A(B(j,1)-1,B(j,2)+1)>0 && A(B(j,1)-2,B(j,2))==1)
    A(B(j,1),B(j,2))=1;
    A(B(j,1)-2,B(j,2))=B(j,3);
    movement(i,j)=8;
elseif (movement(i-1,j)==2 && A(B(j,1)+1,B(j,2))<1 && A(B(j,1)+1,B(j,2))>0 && A(B(j,1),B(j,2)+1)==-1 && A(B(j,1),B(j,2)-1)==-1 && A(B(j,1)+1,B(j,2)-1)<1 && A(B(j,1)+1,B(j,2)-1)>0 && A(B(j,1)+1,B(j,2)+1)<1 && A(B(j,1)+1,B(j,2)+1)>0 && A(B(j,1)+2,B(j,2))==1)
    A(B(j,1),B(j,2))=1;
    A(B(j,1)+2,B(j,2))=B(j,3);
    movement(i,j)=2;
    %con
elseif (movement(i-1)==4 && A(B(j,1),B(j,2)-1)<1 && A(B(j,1),B(j,2)-1)>0 && A(B(j,1)-1,B(j,2))==-1 && A(B(j,1)+1,B(j,2))==-1 && A(B(j,1)-1,B(j,2)-1)<1 && A(B(j,1)-1,B(j,2)-1)>0 && A(B(j,1)-1,B(j,2)+1)<1 && A(B(j,1)-1,B(j,2)+1)>0 && A(B(j,1),B(j,2)-2)==1)
    A(B(j,1),B(j,2))=1;
    A(B(j,1),B(j,2)-2)=B(j,3);
    movement(i,j)=4;
elseif (movement(i-1,j)==6 && A(B(j,1),B(j,2)+1)<1 && A(B(j,1),B(j,2)+1)>0 && A(B(j,1)-1,B(j,2))==-1 && A(B(j,1)+1,B(j,2))==-1 && A(B(j,1)+1,B(j,2)-1)<1 && A(B(j,1)+1,B(j,2)-1)>0 && A(B(j,1)+1,B(j,2)+1)<1 && A(B(j,1)+1,B(j,2)+1)>0 && A(B(j,1),B(j,2)+2)==1)
    A(B(j,1),B(j,2))=1;
    A(B(j,1),B(j,2)+2)=B(j,3);
    movement(i,j)=6; 
end
end

